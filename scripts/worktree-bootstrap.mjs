#!/usr/bin/env node
/**
 * Bootstrap a new git worktree for brain-app: symlink shared .env, .cache/enron tarball,
 * and ./data (never copy), optional .env.local hints, pnpm install.
 *
 * Usage (from primary clone):
 *   node scripts/worktree-bootstrap.mjs ../brain-app-wt-my-feature feat/my-feature
 *   node scripts/worktree-bootstrap.mjs ../brain-app-wt-my-feature feat/my-feature --create
 *   node scripts/worktree-bootstrap.mjs ../brain-app-wt-my-feature feat/my-feature --own-data
 */
import { execFileSync, spawnSync } from 'node:child_process'
import { existsSync, lstatSync, readlinkSync, symlinkSync, unlinkSync, writeFileSync } from 'node:fs'
import { dirname, join, relative, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const primaryRoot = join(__dirname, '..')

const [targetDirArg, branchArg, ...flags] = process.argv.slice(2)
if (!targetDirArg || !branchArg) {
  console.error(
    'Usage: node scripts/worktree-bootstrap.mjs <path> <branch> [--create] [--own-data]\n' +
      '  --create    runs: git worktree add <path> -b <branch> origin/main\n' +
      '  --own-data  keep a separate ./data in this worktree (default: symlink to primary ./data)',
  )
  process.exit(1)
}

const create = flags.includes('--create')
const ownData = flags.includes('--own-data')
const targetDir = resolve(targetDirArg)
const branch = branchArg

const primaryData = join(primaryRoot, 'data')
const wtData = join(targetDir, 'data')

if (create) {
  execFileSync('git', ['worktree', 'add', targetDir, '-b', branch, 'origin/main'], {
    cwd: primaryRoot,
    stdio: 'inherit',
  })
} else if (!existsSync(join(targetDir, '.git'))) {
  console.error(`Not a worktree: ${targetDir} (pass --create to add one)`)
  process.exit(1)
}

const primaryEnv = join(primaryRoot, '.env')
const wtEnv = join(targetDir, '.env')
const wtEnvLocal = join(targetDir, '.env.local')

if (existsSync(primaryEnv) && !existsSync(wtEnv)) {
  symlinkSync(primaryEnv, wtEnv)
  console.log(`Linked .env → ${primaryEnv}`)
}

linkSharedEnronTarballCache(primaryRoot, targetDir)

if (ownData) {
  console.log('--own-data: ./data stays local to this worktree (not symlinked to primary)')
} else {
  linkSharedDataDir(primaryData, targetDir, wtData)
}

const devNote = [
  '# Generated by scripts/worktree-bootstrap.mjs (see .cursor/skills/worktree/SKILL.md)',
  '# Dev: http://127.0.0.1:3000 — set PORT in this file if the primary clone already uses 3000.',
  '# Google OAuth: use the primary clone, or Enron demo on side trees (BRAIN_ENRON_DEMO_SECRET).',
  '',
].join('\n')

if (!existsSync(wtEnvLocal)) {
  writeFileSync(wtEnvLocal, devNote)
  console.log(`Wrote ${wtEnvLocal}`)
} else {
  console.log(`Kept existing ${wtEnvLocal}`)
}

console.log('\nNext (in worktree):')
console.log(`  cd ${targetDir}`)
console.log('  nvm use && corepack enable && pnpm install --frozen-lockfile')
console.log('  pnpm run dev')
console.log('  pnpm run dev:open')

const install = spawnSync('pnpm', ['install', '--frozen-lockfile'], {
  cwd: targetDir,
  stdio: 'inherit',
  env: process.env,
})
process.exit(install.status ?? 1)

/**
 * @param {string} primaryData
 * @param {string} targetDir
 * @param {string} wtData
 */
/**
 * Share the ~1.7 GiB Enron tarball (repo `.cache/enron/`) with the primary clone so worktrees
 * do not re-download. Ingest still writes per-tenant mail under ./data unless --own-data.
 *
 * @param {string} primaryRoot
 * @param {string} targetDir
 */
function linkSharedEnronTarballCache(primaryRoot, targetDir) {
  const primaryCache = join(primaryRoot, '.cache', 'enron')
  const wtCacheParent = join(targetDir, '.cache')
  const wtCache = join(wtCacheParent, 'enron')

  if (!existsSync(primaryCache)) {
    console.warn(
      `[worktree-bootstrap] primary .cache/enron missing (${primaryCache}). ` +
        'Run pnpm run brain:seed-enron-demo once in the primary clone to populate the tarball cache.',
    )
  }

  if (existsSync(wtCache)) {
    const stat = lstatSync(wtCache)
    if (stat.isSymbolicLink()) {
      const resolved = resolve(targetDir, readlinkSync(wtCache))
      if (resolved === resolve(primaryCache)) {
        console.log(`.cache/enron already symlinked → ${relative(targetDir, primaryCache)}`)
        return
      }
      unlinkSync(wtCache)
    } else {
      console.warn(
        `[worktree-bootstrap] ${wtCache} exists as a real directory (e.g. partial download) — removing to symlink primary cache.`,
      )
      execFileSync('rm', ['-rf', wtCache])
    }
  }

  if (!existsSync(wtCacheParent)) {
    execFileSync('mkdir', ['-p', wtCacheParent])
  }
  const relTarget = relative(wtCacheParent, primaryCache)
  symlinkSync(relTarget, wtCache)
  console.log(`Linked .cache/enron → ${relTarget} (shared tarball; no re-download)`)
}

function linkSharedDataDir(primaryData, targetDir, wtData) {
  if (!existsSync(primaryData)) {
    console.warn(
      `[worktree-bootstrap] primary ./data missing (${primaryData}). ` +
        'Run pnpm run dev once in the primary clone, then re-run bootstrap to symlink.',
    )
  }

  if (existsSync(wtData)) {
    const stat = lstatSync(wtData)
    if (stat.isSymbolicLink()) {
      const current = readlinkSync(wtData)
      const resolved = resolve(targetDir, current)
      if (resolved === resolve(primaryData)) {
        console.log(`./data already symlinked → ${relative(targetDir, primaryData)}`)
        return
      }
      unlinkSync(wtData)
    } else {
      console.warn(
        `[worktree-bootstrap] ${wtData} exists as a real directory — not replacing. ` +
          'Remove it and re-run bootstrap to symlink, or pass --own-data to keep a local copy.',
      )
      return
    }
  }

  const relTarget = relative(targetDir, primaryData)
  symlinkSync(relTarget, wtData)
  console.log(`Linked ./data → ${relTarget} (shared with primary; symlink only, never copy)`)
}
