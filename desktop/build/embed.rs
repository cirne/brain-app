//! Build-time embedding of allowlisted API keys from workspace `.env`.
//! Uses `BRAIN_EMBED_MASTER_KEY` from the process environment or from that same `.env` file.

use aes_gcm::aead::Aead;
use aes_gcm::{Aes256Gcm, KeyInit, Nonce};
use hkdf::Hkdf;
use sha2::Sha256;
use std::env;
use std::fs;
use std::io::Write;
use std::path::Path;

/// LLM keys (pi-ai `PROVIDER_API_KEY` convention), tool keys, and bundled Gmail OAuth —
/// must match what `embedded::apply_embedded_env` applies for the Node child.
pub const ALLOWLIST: &[&str] = &[
    "ANTHROPIC_API_KEY",
    "OPENAI_API_KEY",
    "GOOGLE_API_KEY",
    "MISTRAL_API_KEY",
    "XAI_API_KEY",
    "OPENROUTER_API_KEY",
    "COHERE_API_KEY",
    "EXA_API_KEY",
    "SUPADATA_API_KEY",
    "GOOGLE_OAUTH_CLIENT_ID",
    "GOOGLE_OAUTH_CLIENT_SECRET",
];

fn parse_dotenv(contents: &str) -> std::collections::HashMap<String, String> {
    let mut map = std::collections::HashMap::new();
    for line in contents.lines() {
        let trimmed = line.trim();
        if trimmed.is_empty() || trimmed.starts_with('#') {
            continue;
        }
        if let Some(eq) = trimmed.find('=') {
            let key = trimmed[..eq].trim().to_string();
            let mut val = trimmed[eq + 1..].trim().to_string();
            if (val.starts_with('"') && val.ends_with('"'))
                || (val.starts_with('\'') && val.ends_with('\''))
            {
                val = val[1..val.len() - 1].to_string();
            }
            if !val.is_empty() {
                map.insert(key, val);
            }
        }
    }
    map
}

fn derive_aes_key(master: &str) -> [u8; 32] {
    let hk = Hkdf::<Sha256>::new(Some(b"brain-app-embed-v1"), master.as_bytes());
    let mut okm = [0u8; 32];
    hk.expand(b"aes-256-gcm", &mut okm).expect("hkdf expand");
    okm
}

pub fn run() {
    let manifest_dir = env::var("CARGO_MANIFEST_DIR").expect("CARGO_MANIFEST_DIR");
    let out_dir = env::var("OUT_DIR").expect("OUT_DIR");
    let env_path = Path::new(&manifest_dir).join("../.env");

    let dotenv = fs::read_to_string(&env_path).unwrap_or_default();
    let vars = parse_dotenv(&dotenv);
    // Rebuild when .env changes (e.g. user adds BRAIN_EMBED_MASTER_KEY or allowlisted keys).
    println!("cargo:rerun-if-changed={}", env_path.display());

    let master = match env::var("BRAIN_EMBED_MASTER_KEY") {
        Ok(m) if !m.trim().is_empty() => m,
        _ => match vars.get("BRAIN_EMBED_MASTER_KEY") {
            Some(m) if !m.trim().is_empty() => m.clone(),
            _ => {
                println!("cargo:warning=BRAIN_EMBED_MASTER_KEY unset (not in env or workspace .env); embedded API keys will be empty (dev OK)");
                write_empty_payload(&out_dir);
                return;
            }
        },
    };

    let key_bytes = derive_aes_key(&master);
    let cipher = Aes256Gcm::new_from_slice(&key_bytes).expect("aes key");

    let mut entries: Vec<(String, Vec<u8>)> = Vec::new();
    for &name in ALLOWLIST {
        if let Some(val) = vars.get(name) {
            let mut nonce = [0u8; 12];
            getrandom::getrandom(&mut nonce).expect("nonce");
            let n = Nonce::from_slice(&nonce);
            let ct = cipher.encrypt(n, val.as_bytes()).expect("encrypt");
            let mut blob = Vec::with_capacity(12 + ct.len());
            blob.extend_from_slice(&nonce);
            blob.extend_from_slice(&ct);
            entries.push((name.to_string(), blob));
        }
    }

    // Obfuscate key material for runtime (split XOR mask — not real security, matches spike)
    let mask: u8 = 0xA5;
    let mut obf_key = key_bytes;
    for b in obf_key.iter_mut() {
        *b ^= mask;
    }

    let gen_path = Path::new(&out_dir).join("embed_secrets.rs");
    let mut f = fs::File::create(&gen_path).expect("create embed_secrets.rs");
    writeln!(
        f,
        "// @generated by build/embed.rs — do not edit\n\
         pub const OBFUSCATED_DERIVED_KEY: [u8; 32] = {:?};\n",
        obf_key
    )
    .unwrap();

    writeln!(f, "pub const EMBEDDED_BLOBS: &[(&str, &[u8])] = &[").unwrap();
    for (i, (name, blob)) in entries.iter().enumerate() {
        let bin_path = Path::new(&out_dir).join(format!("embed_{i}.bin"));
        fs::write(&bin_path, blob).expect("write blob");
        let abs = fs::canonicalize(&bin_path).expect("canonicalize");
        writeln!(
            f,
            "    (\"{}\", include_bytes!(r\"{}\")),",
            name,
            abs.display()
        )
        .unwrap();
    }
    writeln!(f, "];").unwrap();

    println!("cargo:rerun-if-env-changed=BRAIN_EMBED_MASTER_KEY");
}

fn write_empty_payload(out_dir: &str) {
    let gen_path = Path::new(out_dir).join("embed_secrets.rs");
    let mut f = fs::File::create(&gen_path).expect("create embed_secrets.rs");
    writeln!(
        f,
        "// @generated — no master key at build time\n\
         pub const OBFUSCATED_DERIVED_KEY: [u8; 32] = [0u8; 32];\n\
         pub const EMBEDDED_BLOBS: &[(&str, &[u8])] = &[];\n"
    )
    .unwrap();
}
